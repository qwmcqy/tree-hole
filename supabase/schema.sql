-- Supabase Postgres schema for 树洞 MVP
-- 执行位置：Supabase Dashboard -> SQL Editor
-- 说明：
-- - 使用 auth.users 作为账号来源（Supabase Auth）
-- - 业务表在 public schema
-- - 为了让前端/Server Actions 无需显式写 author_user_id，使用 DEFAULT auth.uid()

-- ========== Extensions ==========
create extension if not exists pgcrypto;

-- ========== Dictionaries ==========
create table if not exists public.categories (
  id bigint generated by default as identity primary key,
  name text not null unique,
  is_active boolean not null default true,
  sort_order int not null default 0
);

create table if not exists public.report_reasons (
  id smallint primary key,
  label text not null,
  is_active boolean not null default true
);

-- ========== Content ==========
create table if not exists public.posts (
  id bigint generated by default as identity primary key,
  author_user_id uuid not null default auth.uid() references auth.users(id),
  category_id bigint not null references public.categories(id),
  title text,
  body text not null,
  status text not null default 'normal',
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  constraint ck_posts_status check (status in ('normal','deleted','folded'))
);

create table if not exists public.comments (
  id bigint generated by default as identity primary key,
  post_id bigint not null references public.posts(id) on delete cascade,
  author_user_id uuid not null default auth.uid() references auth.users(id),
  body text not null,
  status text not null default 'normal',
  created_at timestamptz not null default now(),
  constraint ck_comments_status check (status in ('normal','deleted'))
);

-- 互动：点赞/抱抱二选一（用户对同一帖子唯一）
create table if not exists public.post_reactions (
  id bigint generated by default as identity primary key,
  post_id bigint not null references public.posts(id) on delete cascade,
  user_id uuid not null default auth.uid() references auth.users(id),
  reaction_type text not null,
  created_at timestamptz not null default now(),
  constraint ck_reaction_type check (reaction_type in ('like','hug')),
  constraint uq_post_reaction unique (post_id, user_id)
);

-- 举报：可举报帖子或评论（二选一）
create table if not exists public.reports (
  id bigint generated by default as identity primary key,
  reporter_user_id uuid not null default auth.uid() references auth.users(id),
  post_id bigint references public.posts(id) on delete cascade,
  comment_id bigint references public.comments(id) on delete cascade,
  reason_id smallint not null references public.report_reasons(id),
  note text,
  status text not null default 'pending',
  created_at timestamptz not null default now(),
  handled_at timestamptz,
  constraint ck_reports_status check (status in ('pending','handled')),
  constraint ck_reports_target_one check (
    (post_id is not null and comment_id is null) or
    (post_id is null and comment_id is not null)
  )
);

-- 封禁：被封禁用户禁止写入（发帖/评论/互动/举报）
create table if not exists public.user_bans (
  user_id uuid primary key references auth.users(id) on delete cascade,
  is_active boolean not null default true,
  banned_at timestamptz not null default now(),
  banned_by uuid references auth.users(id),
  note text
);

-- ========== updated_at trigger ==========
create or replace function public.set_updated_at()
returns trigger
language plpgsql
as $$
begin
  new.updated_at = now();
  return new;
end;
$$;

drop trigger if exists trg_posts_updated_at on public.posts;
create trigger trg_posts_updated_at
before update on public.posts
for each row execute function public.set_updated_at();

-- ========== Stats views ==========
create or replace view public.post_stats as
select
  p.id as post_id,
  coalesce(c.comment_count, 0) as comment_count,
  coalesce(r.like_count, 0) as like_count,
  coalesce(r.hug_count, 0) as hug_count
from public.posts p
left join (
  select post_id, count(*)::int as comment_count
  from public.comments
  where status = 'normal'
  group by post_id
) c on c.post_id = p.id
left join (
  select
    post_id,
    count(*) filter (where reaction_type = 'like')::int as like_count,
    count(*) filter (where reaction_type = 'hug')::int as hug_count
  from public.post_reactions
  group by post_id
) r on r.post_id = p.id;

create or replace view public.post_list as
select
  p.id,
  p.title,
  p.body,
  p.created_at,
  p.category_id,
  cat.name as category_name,
  p.status,
  s.comment_count,
  s.like_count,
  s.hug_count
from public.posts p
join public.categories cat on cat.id = p.category_id
left join public.post_stats s on s.post_id = p.id;

