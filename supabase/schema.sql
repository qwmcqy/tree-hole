-- 公开可读（MVP）
drop policy if exists "categories_read" on public.categories;
create policy "categories_read" on public.categories
for select using (is_active = true);

drop policy if exists "report_reasons_read" on public.report_reasons;
create policy "report_reasons_read" on public.report_reasons
for select using (is_active = true);

drop policy if exists "posts_read" on public.posts;
create policy "posts_read" on public.posts
for select using (status = 'normal');

drop policy if exists "comments_read" on public.comments;
create policy "comments_read" on public.comments
for select using (status = 'normal');

-- 写入需登录
drop policy if exists "posts_insert" on public.posts;
create policy "posts_insert" on public.posts
for insert to authenticated
with check (
  auth.uid() = author_user_id
  and not exists (
    select 1
    from public.user_bans b
    where b.user_id = auth.uid() and b.is_active = true
  )
);

drop policy if exists "comments_insert" on public.comments;
create policy "comments_insert" on public.comments
for insert to authenticated
with check (
  auth.uid() = author_user_id
  and not exists (
    select 1
    from public.user_bans b
    where b.user_id = auth.uid() and b.is_active = true
  )
);

drop policy if exists "reactions_upsert" on public.post_reactions;
create policy "reactions_upsert" on public.post_reactions
for insert to authenticated
with check (
  auth.uid() = user_id
  and not exists (
    select 1
    from public.user_bans b
    where b.user_id = auth.uid() and b.is_active = true
  )
);

drop policy if exists "reactions_read_own" on public.post_reactions;
create policy "reactions_read_own" on public.post_reactions
for select to authenticated
using (auth.uid() = user_id);

drop policy if exists "reactions_update_own" on public.post_reactions;
create policy "reactions_update_own" on public.post_reactions
for update to authenticated
using (auth.uid() = user_id)
with check (auth.uid() = user_id);

drop policy if exists "reactions_delete_own" on public.post_reactions;
create policy "reactions_delete_own" on public.post_reactions
for delete to authenticated
using (auth.uid() = user_id);

drop policy if exists "reports_insert" on public.reports;
create policy "reports_insert" on public.reports
for insert to authenticated
with check (
  auth.uid() = reporter_user_id
  and not exists (
    select 1
    from public.user_bans b
    where b.user_id = auth.uid() and b.is_active = true
  )
);

-- 封禁表：用户仅可读取自己的封禁状态（用于前端提示可选）
drop policy if exists "user_bans_read_own" on public.user_bans;
create policy "user_bans_read_own" on public.user_bans
for select to authenticated
using (user_id = auth.uid() and is_active = true);
